async function handleBetlogCsvImport() {
  const input = document.querySelector("#betlog-csv");
  const [file] = input.files || [];
  if (!file) {
    showScopedMessage("#betlog-import-error", "Choose a CSV file first.");
    return;
  }

  try {
    const rows = parseBetlogCsvRows(await file.text());
    const bets = rows.map(betlogBetFromCsvRow);
    const payload = await request("/tools/betlog/bets", {
      method: "POST",
      body: JSON.stringify({ bets }),
    });
    betlogBets = payload.bets || [];
    betlogHistoryPage = 1;
    renderBetlogBets();
    showScopedMessage("#betlog-import-success", `Imported ${payload.imported || bets.length} bet rows.`);
  } catch (error) {
    showScopedMessage("#betlog-import-error", error.message);
  }
}

