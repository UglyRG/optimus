function formatAppVersion(version) {
  const cleanVersion = String(version || "")
    .replace(/-dirty$/, "")
    .replace(/^v?/, "")
    .trim();
  const [major = "4", minor = "5"] = cleanVersion.split(".");
  return `v.${major}.${minor}`;
}

