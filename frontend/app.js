async function handleBetlogTableClick(event) {
  const editButton = event.target.closest("[data-edit-betlog-bet]");
  if (editButton) {
    editingBetlogBetId = editButton.dataset.editBetlogBet;
    renderBetlogBets();
    return;
  }

  const cancelButton = event.target.closest("[data-cancel-betlog-edit]");
  if (cancelButton) {
    editingBetlogBetId = "";
    renderBetlogBets();
    return;
  }

  const saveButton = event.target.closest("[data-save-betlog-bet]");
  if (saveButton) {
    await saveBetlogBetEdit(saveButton.dataset.saveBetlogBet);
    return;
  }

  const deleteButton = event.target.closest("[data-delete-betlog-bet]");
  if (!deleteButton) {
    return;
  }

  try {
    const payload = await request("/tools/betlog/delete", {
      method: "POST",
      body: JSON.stringify({ id: deleteButton.dataset.deleteBetlogBet }),
    });
    betlogBets = payload.bets || [];
    if (editingBetlogBetId === deleteButton.dataset.deleteBetlogBet) {
      editingBetlogBetId = "";
    }
    renderBetlogBets();
  } catch (error) {
    showScopedMessage("#betlog-history-error", error.message);
  }
}

