async function handleBetlogManualSubmit(event) {
  event.preventDefault();
  const form = event.currentTarget;
  const data = new FormData(form);
  const bet = {
    date: String(data.get("date")).trim(),
    time: String(data.get("time")).trim(),
    betId: String(data.get("betId")).trim(),
    betType: String(data.get("betType")).trim(),
    stake: String(data.get("stake")).trim(),
    freeBet: data.get("freeBet") === "on",
    status: String(data.get("status")).trim(),
    returnAmount: String(data.get("returnAmount")).trim(),
    selection: String(data.get("selection")).trim(),
    odds: String(data.get("odds")).trim(),
    market: String(data.get("market")).trim(),
    match: String(data.get("match")).trim(),
    score: String(data.get("score")).trim(),
    outcomeType: String(data.get("outcomeType")).trim(),
    legs: String(data.get("legs")).trim(),
  };

  try {
    const payload = await request("/tools/betlog/bets", {
      method: "POST",
      body: JSON.stringify({ bet }),
    });
    betlogBets = payload.bets || [];
    betlogHistoryPage = 1;
    form.reset();
    document.querySelector("#betlog-date").value = localDateInputValue();
    document.querySelector("#betlog-time").value = localTimeInputValue();
    document.querySelector("#betlog-return").value = "0";
    document.querySelector("#betlog-outcome-type").value = "single";
    document.querySelector("#betlog-legs").value = "1";
    renderBetlogBets();
    showScopedMessage("#betlog-manual-success", "Bet saved.");
  } catch (error) {
    showScopedMessage("#betlog-manual-error", error.message);
  }
}

