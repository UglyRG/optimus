function renderNotelogCanvas() {
  const canvas = document.querySelector("#notelog-canvas");
  const page = activeNotelogPage();
  if (!canvas || !page) {
    return;
  }

  canvas.width = page.width || NOTELOG_PAGE_WIDTH;
  canvas.height = page.height || NOTELOG_PAGE_HEIGHT;
  const context = canvas.getContext("2d");
  drawNotelogPage(context, page);
}

function drawNotelogPage(context, page) {
  context.save();
  context.clearRect(0, 0, page.width, page.height);
  context.fillStyle = "#ffffff";
  context.fillRect(0, 0, page.width, page.height);
  drawNotelogBackground(context, page);
  for (const stroke of page.strokes || []) {
    drawNotelogStroke(context, stroke);
  }
  context.restore();
}

function drawNotelogBackground(context, page) {
  context.save();
  context.strokeStyle = "#dbe7f6";
  context.fillStyle = "#dbe7f6";
  context.lineWidth = 1;
  if (page.background === "ruled" || page.background === "cornell" || page.background === "meeting") {
    for (let y = 96; y < page.height; y += 44) {
      context.beginPath();
      context.moveTo(72, y);
      context.lineTo(page.width - 72, y);
      context.stroke();
    }
  } else if (page.background === "dots") {
    for (let y = 52; y < page.height; y += 32) {
      for (let x = 52; x < page.width; x += 32) {
        context.beginPath();
        context.arc(x, y, 1.3, 0, Math.PI * 2);
        context.fill();
      }
    }
  } else if (page.background === "grid") {
    for (let x = 50; x < page.width; x += 32) {
      context.beginPath();
      context.moveTo(x, 0);
      context.lineTo(x, page.height);
      context.stroke();
    }
    for (let y = 50; y < page.height; y += 32) {
      context.beginPath();
      context.moveTo(0, y);
      context.lineTo(page.width, y);
      context.stroke();
    }
  }

  context.strokeStyle = "#c6d4ea";
  context.lineWidth = 2;
  if (page.background === "cornell") {
    context.beginPath();
    context.moveTo(330, 70);
    context.lineTo(330, page.height - 190);
    context.moveTo(72, page.height - 190);
    context.lineTo(page.width - 72, page.height - 190);
    context.stroke();
  } else if (page.background === "meeting") {
    context.beginPath();
    context.moveTo(72, 92);
    context.lineTo(page.width - 72, 92);
    context.moveTo(72, 150);
    context.lineTo(page.width - 72, 150);
    context.moveTo(page.width - 360, 92);
    context.lineTo(page.width - 360, 150);
    context.moveTo(page.width - 360, 210);
    context.lineTo(page.width - 360, page.height - 72);
    context.moveTo(page.width - 360, page.height - 220);
    context.lineTo(page.width - 72, page.height - 220);
    context.stroke();
  }
  context.restore();
}

function drawNotelogStroke(context, stroke) {
  const points = stroke.points || [];
  if (points.length < 2) {
    return;
  }

  context.save();
  context.lineCap = "round";
  context.lineJoin = "round";
  context.strokeStyle = stroke.tool === "eraser" ? "#ffffff" : stroke.color || "#111827";
  context.globalCompositeOperation = "source-over";

  for (let index = 1; index < points.length; index += 1) {
    const previous = points[index - 1];
    const current = points[index];
    const pressure = Math.max(0.22, ((previous.pressure || 0.5) + (current.pressure || 0.5)) / 2);
    context.lineWidth = Math.max(0.8, (stroke.size || 4) * (0.55 + pressure));
    context.beginPath();
    context.moveTo(previous.x, previous.y);
    context.lineTo(current.x, current.y);
    context.stroke();
  }
  context.restore();
}

