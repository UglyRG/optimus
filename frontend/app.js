function parseCsvRows(csvText) {
  return parseCsvRowsWithRequiredHeaders(csvText, ["Padel Club", "Date", "Opponents", "Result", "Sets"], {
    oneOf: [["Teamate", "Teammate"]],
    oneOfLabel: "Teamate",
    emptyMessage: "CSV needs a header row and at least one match.",
  });
}

function parseCsvRowsWithRequiredHeaders(csvText, requiredHeaders, options = {}) {
  const rows = parseCsvText(csvText).filter((row) => row.some((cell) => String(cell).trim()));
  if (rows.length < 2) {
    throw new Error(options.emptyMessage || "CSV needs a header row and at least one data row.");
  }

  const headers = rows[0].map((header) => String(header).trim());
  const missingHeaders = requiredHeaders.filter((header) => !headers.includes(header));
  if (options.oneOf) {
    options.oneOf.forEach((acceptedHeaders, index) => {
      if (!headers.some((header) => acceptedHeaders.includes(header))) {
        missingHeaders.push(Array.isArray(options.oneOfLabel) ? options.oneOfLabel[index] : options.oneOfLabel);
      }
    });
  }
  if (missingHeaders.length) {
    throw new Error(`CSV is missing: ${missingHeaders.join(", ")}`);
  }

  return rows.slice(1).map((cells) =>
    Object.fromEntries(headers.map((header, index) => [header, String(cells[index] || "").trim()])),
  );
}

function parseCsvText(text) {
  const rows = [];
  let row = [];
  let value = "";
  let inQuotes = false;

  for (let index = 0; index < text.length; index += 1) {
    const character = text[index];
    const nextCharacter = text[index + 1];
    if (character === '"' && inQuotes && nextCharacter === '"') {
      value += '"';
      index += 1;
    } else if (character === '"') {
      inQuotes = !inQuotes;
    } else if (character === "," && !inQuotes) {
      row.push(value);
      value = "";
    } else if ((character === "\n" || character === "\r") && !inQuotes) {
      if (character === "\r" && nextCharacter === "\n") {
        index += 1;
      }
      row.push(value);
      rows.push(row);
      row = [];
      value = "";
    } else {
      value += character;
    }
  }

  row.push(value);
  rows.push(row);
  return rows;
}

