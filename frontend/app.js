function renderBetlogBets() {
  const table = document.querySelector("#betlog-bet-table");
  const count = document.querySelector("#betlog-bet-count");
  const pagination = document.querySelector("#betlog-pagination");
  if (!table || !count || !pagination) {
    return;
  }

  count.textContent = `${betlogBets.length} saved bet row${betlogBets.length === 1 ? "" : "s"}.`;
  if (!betlogBets.length) {
    table.innerHTML = '<tr><td colspan="7">No bets yet.</td></tr>';
    pagination.innerHTML = "";
    updateBetlogStats();
    return;
  }

  const displayBets = betlogHistoryBetsForDisplay();
  const pageSize = betlogHistoryPageSize();
  const totalPages = pageSize === Infinity ? 1 : Math.max(1, Math.ceil(displayBets.length / pageSize));
  betlogHistoryPage = Math.min(Math.max(1, betlogHistoryPage), totalPages);
  const startIndex = pageSize === Infinity ? 0 : (betlogHistoryPage - 1) * pageSize;
  const pageBets = pageSize === Infinity ? displayBets : displayBets.slice(startIndex, startIndex + pageSize);
  const endIndex = startIndex + pageBets.length;

  count.textContent = `${betlogBets.length} saved bet row${betlogBets.length === 1 ? "" : "s"} · showing ${startIndex + 1}-${endIndex}.`;
  table.innerHTML = renderBetlogGroupedRows(pageBets);
  pagination.innerHTML = renderBetlogPagination(totalPages);
  updateBetlogStats();
}

function betlogHistoryBetsForDisplay() {
  const groupBy = document.querySelector("#betlog-history-group")?.value || "month";
  if (groupBy === "status") {
    return [...betlogBets].sort(
      (left, right) =>
        String(left.status || "").localeCompare(String(right.status || "")) ||
        right.date.localeCompare(left.date) ||
        right.time.localeCompare(left.time),
    );
  }
  if (groupBy === "betType") {
    return [...betlogBets].sort(
      (left, right) =>
        String(left.betType || "").localeCompare(String(right.betType || "")) ||
        right.date.localeCompare(left.date) ||
        right.time.localeCompare(left.time),
    );
  }

  return betlogBets;
}

function renderBetlogGroupedRows(bets) {
  const groupBy = document.querySelector("#betlog-history-group")?.value || "month";
  let activeGroup = "";

  return bets
    .map((bet) => {
      const group = betlogHistoryGroupLabel(bet, groupBy);
      const groupRow = group && group !== activeGroup
        ? `<tr class="padelog-group-row"><td colspan="7">${escapeHtml(group)}</td></tr>`
        : "";
      activeGroup = group || activeGroup;
      return `
        ${groupRow}
        ${editingBetlogBetId === bet.id ? renderBetlogEditRow(bet) : renderBetlogDisplayRow(bet)}
      `;
    })
    .join("");
}

